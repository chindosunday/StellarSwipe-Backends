const EXPANDED_RISK_CASES = RISK_THRESHOLD_MATRIX.flatMap((config) =>
  expandRiskCases(config),
);

describe('RiskControlsService regression matrix', () => {
  afterEach(() => {
    jest.clearAllMocks();
  });

  it.each(EXPANDED_RISK_CASES)(
    '$label - $caseName',
    async (riskCase: ExpandedRiskCase) => {
      const trade = makeTrade({
        baseAsset: riskCase.baseAsset,
        counterAsset: riskCase.counterAsset,
        entryPrice: riskCase.entryPrice,
        stopLossPrice: riskCase.stopLossPrice,
        takeProfitPrice: riskCase.takeProfitPrice,
      });
      const harness = makeHarness([trade]);
      harness.priceService.getCurrentPrice.mockResolvedValue(
        riskCase.currentPrice,
      );

      await harness.service.enforceRiskLevels();

      expect(harness.priceService.getCurrentPrice).toHaveBeenCalledWith(
        `${riskCase.baseAsset}/${riskCase.counterAsset}`,
      );

      if (riskCase.expectedTrigger === null) {
        expect(harness.tradeExecutor.closeTrade).not.toHaveBeenCalled();
        expect(harness.tradeRepository.save).not.toHaveBeenCalled();
        expect(harness.notificationService.send).not.toHaveBeenCalled();
        return;
      }

      const currentPriceText = riskCase.currentPrice.toString();

      expect(harness.tradeExecutor.closeTrade).toHaveBeenCalledWith(
        trade,
        currentPriceText,
      );
      expect(harness.tradeRepository.save).toHaveBeenCalledWith(
        expect.objectContaining({
          id: trade.id,
          exitPrice: currentPriceText,
          metadata: expect.objectContaining({
            closedBy: riskCase.expectedTrigger,
          }),
        }),
      );
      expect(harness.notificationService.send).toHaveBeenCalledWith(
        expect.objectContaining({
          userId: trade.userId,
          channel: NotificationChannel.IN_APP,
          type:
            riskCase.expectedTrigger === 'stop_loss'
              ? 'STOP_LOSS_TRIGGERED'
              : 'TAKE_PROFIT_TRIGGERED',
          metadata: expect.objectContaining({
            tradeId: trade.id,
            triggerPrice: currentPriceText,
            type: riskCase.expectedTrigger,
          }),
        }),
      );
    },
  );

  it('does not let a safe trade block a later triggered trade in the same scheduler tick', async () => {
    const safeTrade = makeTrade({
      id: 'safe-trade',
      baseAsset: 'XLM',
      counterAsset: 'USDC',
      stopLossPrice: '0.12000000',
      takeProfitPrice: '0.18000000',
    });
    const triggeredTrade = makeTrade({
      id: 'triggered-trade',
      baseAsset: 'ETH',
      counterAsset: 'USDC',
      stopLossPrice: '3150.00000000',
      takeProfitPrice: '4200.00000000',
    });
    const harness = makeHarness([safeTrade, triggeredTrade]);
    harness.priceService.getCurrentPrice.mockImplementation((pair: string) => {
      if (pair === 'XLM/USDC') return Promise.resolve(0.15);
      if (pair === 'ETH/USDC') return Promise.resolve(3149.99);
      return Promise.reject(new Error(`unexpected pair ${pair}`));
    });

    await harness.service.enforceRiskLevels();

    expect(harness.tradeExecutor.closeTrade).toHaveBeenCalledTimes(1);
    expect(harness.tradeExecutor.closeTrade).toHaveBeenCalledWith(
      triggeredTrade,
      '3149.99',
    );
    expect(harness.tradeRepository.save).toHaveBeenCalledWith(
      expect.objectContaining({
        id: 'triggered-trade',
        metadata: expect.objectContaining({ closedBy: 'stop_loss' }),
      }),
    );
  });
});

function expandRiskCases(config: RiskThresholdConfig): ExpandedRiskCase[] {
  return [
    {
      ...config,
      caseName: 'stop-loss below threshold',
      currentPrice: config.stopLossTriggerPrice,
      expectedTrigger: 'stop_loss',
    },
    {
      ...config,
      caseName: 'stop-loss boundary threshold',
      currentPrice: Number(config.stopLossPrice),
      expectedTrigger: 'stop_loss',
    },
    {
      ...config,
      caseName: 'take-profit above threshold',
      currentPrice: config.takeProfitTriggerPrice,
      expectedTrigger: 'take_profit',
    },
    {
      ...config,
      caseName: 'take-profit boundary threshold',
      currentPrice: Number(config.takeProfitPrice),
      expectedTrigger: 'take_profit',
    },
    {
      ...config,
      caseName: 'safe price between thresholds',
      currentPrice: config.safePrice,
      expectedTrigger: null,
    },
  ];
}

function makeHarness(openTrades: Trade[]) {
  const tradeRepository = {
    find: jest.fn().mockResolvedValue(openTrades),
    findOne: jest.fn(),
    save: jest.fn((trade: Trade) => Promise.resolve(trade)),
  };
  const tradeExecutor = {
    closeTrade: jest.fn().mockResolvedValue({ success: true }),
  };
  const notificationService = {
    send: jest.fn().mockResolvedValue(undefined),
  };
  const priceService = {
    getCurrentPrice: jest.fn(),
  };
  const eventEmitter = {
    emit: jest.fn().mockReturnValue(true),
  };
  const service = new RiskControlsService(
    tradeRepository as any,
    tradeExecutor as unknown as TradeExecutorService,
    notificationService as unknown as NotificationService,
    priceService as unknown as PriceService,
    eventEmitter as unknown as EventEmitter2,
  );

  jest
    .spyOn((service as any).logger, 'log')
    .mockImplementation(() => undefined);
  jest
    .spyOn((service as any).logger, 'error')
    .mockImplementation(() => undefined);

  return {
    service,
    tradeRepository,
    tradeExecutor,
    notificationService,
    priceService,
    eventEmitter,
  };
}

